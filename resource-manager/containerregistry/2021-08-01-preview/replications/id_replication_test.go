func TestNewReplicationID(t *testing.T) {
	id := NewReplicationID("12345678-1234-9876-4563-123456789012", "example-resource-group", "registryValue", "replicationValue")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.RegistryName != "registryValue" {
		t.Fatalf("Expected %q but got %q for Segment 'RegistryName'", id.RegistryName, "registryValue")
	}

	if id.ReplicationName != "replicationValue" {
		t.Fatalf("Expected %q but got %q for Segment 'ReplicationName'", id.ReplicationName, "replicationValue")
	}
}

func TestFormatReplicationID(t *testing.T) {
	actual := NewReplicationID("12345678-1234-9876-4563-123456789012", "example-resource-group", "registryValue", "replicationValue").ID()
	expected := "/subscriptions/12345678-1234-9876-4563-123456789012/resourceGroups/example-resource-group/providers/Microsoft.ContainerRegistry/registries/registryValue/replications/replicationValue"
	if actual != expected {
		t.Fatalf("Expected the Formatted ID to be %q but got %q", expected, actual)
	}
}

