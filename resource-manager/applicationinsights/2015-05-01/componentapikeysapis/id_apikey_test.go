var _ resourceids.ResourceId = &ApiKeyId{}

func TestNewApiKeyID(t *testing.T) {
	id := NewApiKeyID("12345678-1234-9876-4563-123456789012", "example-resource-group", "componentValue", "keyIdValue")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.ComponentName != "componentValue" {
		t.Fatalf("Expected %q but got %q for Segment 'ComponentName'", id.ComponentName, "componentValue")
	}

	if id.KeyId != "keyIdValue" {
		t.Fatalf("Expected %q but got %q for Segment 'KeyId'", id.KeyId, "keyIdValue")
	}
}

func TestFormatApiKeyID(t *testing.T) {
	actual := NewApiKeyID("12345678-1234-9876-4563-123456789012", "example-resource-group", "componentValue", "keyIdValue").ID()
	expected := "/subscriptions/12345678-1234-9876-4563-123456789012/resourceGroups/example-resource-group/providers/Microsoft.Insights/components/componentValue/apiKeys/keyIdValue"
	if actual != expected {
		t.Fatalf("Expected the Formatted ID to be %q but got %q", expected, actual)
	}
}

