func TestNewExecutionID(t *testing.T) {
	id := NewExecutionID("12345678-1234-9876-4563-123456789012", "example-resource-group", "serverValue", "jobAgentValue", "jobValue", "jobExecutionIdValue")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.ServerName != "serverValue" {
		t.Fatalf("Expected %q but got %q for Segment 'ServerName'", id.ServerName, "serverValue")
	}

	if id.JobAgentName != "jobAgentValue" {
		t.Fatalf("Expected %q but got %q for Segment 'JobAgentName'", id.JobAgentName, "jobAgentValue")
	}

	if id.JobName != "jobValue" {
		t.Fatalf("Expected %q but got %q for Segment 'JobName'", id.JobName, "jobValue")
	}

	if id.JobExecutionId != "jobExecutionIdValue" {
		t.Fatalf("Expected %q but got %q for Segment 'JobExecutionId'", id.JobExecutionId, "jobExecutionIdValue")
	}
}

func TestFormatExecutionID(t *testing.T) {
	actual := NewExecutionID("12345678-1234-9876-4563-123456789012", "example-resource-group", "serverValue", "jobAgentValue", "jobValue", "jobExecutionIdValue").ID()
	expected := "/subscriptions/12345678-1234-9876-4563-123456789012/resourceGroups/example-resource-group/providers/Microsoft.Sql/servers/serverValue/jobAgents/jobAgentValue/jobs/jobValue/executions/jobExecutionIdValue"
	if actual != expected {
		t.Fatalf("Expected the Formatted ID to be %q but got %q", expected, actual)
	}
}

